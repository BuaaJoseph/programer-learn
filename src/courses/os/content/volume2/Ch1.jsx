const translateCode = `# 假设：页大小 4KB = 4096 字节 = 2^12，所以页内偏移占低 12 位
# 虚拟地址 32 位 = 高 20 位页号 + 低 12 位页内偏移

VADDR    = 0x00403ABC      # 要翻译的虚拟地址
PAGESIZE = 4096            # 2^12

vpn    = VADDR >> 12       # 页号 = 取高 20 位
offset = VADDR & 0xFFF     # 页内偏移 = 取低 12 位

# 查页表，得到该页对应的物理页框号（这一步硬件 MMU 自动做）
page_table = {0x00403: 0x12}     # vpn -> 物理页框号 pfn
pfn = page_table[vpn]

paddr = (pfn << 12) | offset     # 物理地址 = 页框号 拼上 页内偏移
print(hex(vpn), hex(offset), '=>', hex(paddr))
# 0x403 0xabc => 0x12abc`

const localityCode = `// 行优先存储的二维数组，内存里是一行接一行连续排列的
int sum = 0;
for (int i = 0; i < N; i++)
    for (int j = 0; j < M; j++)
        sum += a[i][j];   // 顺着内存方向走：访问一页后，后续多次命中同页

// 把循环换成列优先：a[j][i]
// 每次都跳到很远的地址，频繁跨页，TLB 命中率和缓存命中率都暴跌`

