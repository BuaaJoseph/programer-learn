      <Callout variant="warn" title="别把虚拟地址当物理地址">
        <p>
          常见误区：以为打印出来的指针地址就是内存条上的真实位置。其实你在用户程序里拿到的<strong>全是虚拟地址</strong>，
          它要经过 MMU 翻译才变成物理地址；两个不同进程打印出一模一样的指针值，背后却是完全不同的物理页，互不相干。
          也正因为有这层翻译，操作系统才能随意搬动、换出物理页而不影响程序里写的地址。
        </p>
      </Callout>

